#!/usr/bin/env node

// Reproduce the four makedefs-generated random-access text files which the
// JavaScript runtime needs.  Keeping their encrypted byte layout preserves the
// exact rn2() bounds and offset-selection behavior in rumors.c:get_rnd_line().

import { execFileSync } from 'node:child_process';
import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { xcrypt } from '../js/hacklib.js';

const PROJECT_ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const UPSTREAM_ROOT = join(PROJECT_ROOT, 'nethack-c', 'upstream');
const OUTPUT_PATH = join(PROJECT_ROOT, 'js', 'random_text_data.js');
const PINNED_REVISION = '16ff59115315917b93185d026aeefea06db9b0f4';
const DONT_EDIT = "#\tThis data file is generated by 'makedefs'.  Do not edit. \n";
const PAD_LENGTH = 60;
const BOGUSMON_PAD_LENGTH = 20;

function sourceLines(source) {
    const lines = source.match(/[^\n]*\n|[^\n]+$/gu) ?? [];
    return lines.map((line) => line.endsWith('\n') ? line : `${line}\n`);
}

// C ref: util/makedefs.c padline().  padlength includes the newline in the
// generated file despite get_rnd_line() describing it as the text width.
export function padSourceLine(line, padlength = PAD_LENGTH) {
    if (!line.endsWith('\n'))
        throw new Error('makedefs source line must end in a newline');
    if (line.length > padlength) return line;
    return `${line.slice(0, -1)}${'_'.repeat(padlength - line.length)}\n`;
}

function encryptedLines(lines, padlength = PAD_LENGTH) {
    return lines.map((line) => xcrypt(padSourceLine(line, padlength)))
        .join('');
}

function randomAccessSourceLines(source, enabledSymbols) {
    const result = [];
    let include = true;
    for (const line of sourceLines(source)) {
        if (line.startsWith('^?')) {
            include = enabledSymbols.has(line.slice(2).trim());
            continue;
        }
        if (line.startsWith('^.')) {
            include = true;
            continue;
        }
        if (include && line[0] !== '#' && line !== '\n') result.push(line);
    }
    return result;
}

function buildRandomAccessFile(
    source,
    defaultText,
    enabledSymbols,
    padlength = PAD_LENGTH,
) {
    const lines = [`${defaultText}\n`, ...randomAccessSourceLines(
        source,
        enabledSymbols,
    )];
    return DONT_EDIT + encryptedLines(lines, padlength);
}

function fixedDecimal(value, width) {
    return String(value).padStart(width, '0');
}

function fixedHex(value, width) {
    return value.toString(16).padStart(width, '0');
}

function buildRumorFile(trueSource, falseSource) {
    const trueLines = sourceLines(trueSource);
    const falseLines = sourceLines(falseSource);
    const trueBody = encryptedLines(trueLines);
    const falseBody = encryptedLines(falseLines);
    // The formatted header is fixed width, so the first data offset can be
    // calculated before its numeric fields are populated.
    const emptyHeader = '0000,000000,000000;0000,000000,000000;0,0,000000\n';
    const trueStart = DONT_EDIT.length + emptyHeader.length;
    const falseStart = trueStart + trueBody.length;
    const eof = falseStart + falseBody.length;
    const header = `${fixedDecimal(trueLines.length, 4)},`
        + `${fixedDecimal(trueBody.length, 6)},${fixedHex(trueStart, 6)};`
        + `${fixedDecimal(falseLines.length, 4)},`
        + `${fixedDecimal(falseBody.length, 6)},${fixedHex(falseStart, 6)};`
        + `0,0,${fixedHex(eof, 6)}\n`;
    return DONT_EDIT + header + trueBody + falseBody;
}

function requireAscii(source, sourceName) {
    if (/[^\x00-\x7f]/u.test(source))
        throw new Error(`${sourceName} contains a non-ASCII byte`);
    if (source.includes('\r'))
        throw new Error(`${sourceName} contains a carriage return`);
    return source;
}

export function buildRandomTextFiles(upstreamRoot = UPSTREAM_ROOT) {
    const readSource = (name) => requireAscii(
        readFileSync(join(upstreamRoot, 'dat', name), 'utf8'),
        name,
    );
    const enabledSymbols = new Set(['MAIL']);
    return Object.freeze({
        rumors: buildRumorFile(
            readSource('rumors.tru'),
            readSource('rumors.fal'),
        ),
        engrave: buildRandomAccessFile(
            readSource('engrave.txt'),
            'No matter where you go, there you are.',
            enabledSymbols,
        ),
        epitaph: buildRandomAccessFile(
            readSource('epitaph.txt'),
            'No matter where I went, here I am.',
            enabledSymbols,
        ),
        bogusmon: buildRandomAccessFile(
            readSource('bogusmon.txt'),
            'grue',
            enabledSymbols,
            BOGUSMON_PAD_LENGTH,
        ),
    });
}

function sha256(text) {
    return createHash('sha256').update(text, 'latin1').digest('hex');
}

export function renderRandomTextData(files) {
    const hashes = Object.fromEntries(
        Object.entries(files).map(([name, data]) => [name, sha256(data)]),
    );
    return `// Generated by scripts/generate-random-text-data.mjs.\n`
        + `// Source: NetHack 5.0 dat text at ${PINNED_REVISION}; projected with\n`
        + `// util/makedefs.c padline(), xcrypt(), and random-access-file layout.\n\n`
        + `export const RANDOM_TEXT_FILE_HASHES = Object.freeze(${JSON.stringify(hashes, null, 4)});\n\n`
        + `export const RANDOM_TEXT_FILES = Object.freeze(${JSON.stringify(files, null, 4)});\n`;
}

function assertPinnedSource() {
    const actualRevision = execFileSync(
        'git', ['-C', UPSTREAM_ROOT, 'rev-parse', 'HEAD'], { encoding: 'utf8' },
    ).trim();
    if (actualRevision !== PINNED_REVISION) {
        throw new Error(
            `expected NetHack source ${PINNED_REVISION}; found ${actualRevision}`,
        );
    }
    const upstreamStatus = execFileSync(
        'git', ['-C', UPSTREAM_ROOT, 'status', '--porcelain=v1', '--untracked-files=no'],
        { encoding: 'utf8' },
    ).trim();
    if (upstreamStatus)
        throw new Error('Refusing to generate from modified tracked upstream sources');
}

function main() {
    const checkOnly = process.argv.length === 3 && process.argv[2] === '--check';
    if (process.argv.length > (checkOnly ? 3 : 2)) {
        throw new Error(
            'Usage: node scripts/generate-random-text-data.mjs [--check]',
        );
    }
    assertPinnedSource();
    const output = renderRandomTextData(buildRandomTextFiles());
    if (checkOnly) {
        let existing = '';
        try {
            existing = readFileSync(OUTPUT_PATH, 'utf8');
        } catch {
            // The comparison below reports the missing generated file.
        }
        if (existing !== output) {
            console.error(
                'js/random_text_data.js is stale; run '
                + 'node scripts/generate-random-text-data.mjs',
            );
            process.exitCode = 1;
        }
    } else {
        writeFileSync(OUTPUT_PATH, output);
    }
}

if (process.argv[1]
    && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) main();
